public class ConfigEntry {

    private int cid;

    private String caname;

    private String cdmark;

    private int cpidx;

    private String ccmd;

    private String cavg;

    private String ctime;

    private int cused;

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getCaname() {
        return caname;
    }

    public void setCaname(String caname) {
        this.caname = caname;
    }

    public String getCdmark() {
        return cdmark;
    }

    public void setCdmark(String cdmark) {
        this.cdmark = cdmark;
    }

    public int getCpidx() {
        return cpidx;
    }

    public void setCpidx(int cpidx) {
        this.cpidx = cpidx;
    }

    public String getCcmd() {
        return ccmd;
    }

    public void setCcmd(String ccmd) {
        this.ccmd = ccmd;
    }

    public String getCavg() {
        return cavg;
    }

    public void setCavg(String cavg) {
        this.cavg = cavg;
    }

    public String getCtime() {
        return ctime;
    }

    public void setCtime(String ctime) {
        this.ctime = ctime;
    }

    public int getCused() {
        return cused;
    }

    public void setCused(int cused) {
        this.cused = cused;
    }
}
